const LANGUAGE_MAP: Record<string, Record<string, string>> = {
  en: EN,
  es: ES,
  fr: FR,
  de: DE,
  hi: HI,
  ar: AR,
};

export const SUPPORTED_LANGUAGES = [
  { code: "en", label: "English" },
  { code: "es", label: "Espanol" },
  { code: "fr", label: "Francais" },
  { code: "de", label: "Deutsch" },
  { code: "hi", label: "Hindi" },
  { code: "ar", label: "Arabic" },
] as const;

export const DEFAULT_LABELS = EN;

export function getPortalLabels(
  language: string,
  overrides?: Record<string, string> | null,
): Record<string, string> {
  const base = LANGUAGE_MAP[language] ?? EN;
  const merged = { ...EN, ...base };
  if (overrides && typeof overrides === "object") {
    for (const [key, val] of Object.entries(overrides)) {
      if (typeof val === "string" && val.trim()) {
        merged[key] = val;
      }
    }
  }
  return merged;
}

export function getAllLabelKeys(): string[] {
  return Object.keys(EN);
}

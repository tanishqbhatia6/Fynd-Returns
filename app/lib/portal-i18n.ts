export function getPortalLabels(
  language: string,
  overrides?: Record<string, string> | null,
): Record<string, string> {
  const langBase = language.split("-")[0].toLowerCase();
  const base = LANGUAGE_MAP[langBase] ?? EN;
  const merged = { ...EN, ...base };
  if (overrides && typeof overrides === "object") {
    for (const [key, val] of Object.entries(overrides)) {
      if (typeof val === "string" && val.trim()) {
        merged[key] = val;
      }
    }
  }
  return merged;
}

export function getAllLabelKeys(): string[] {
  return Object.keys(EN);
}

/** Template string interpolation: replaces {key} placeholders */
export function t(
  key: string,
  labels: Record<string, string>,
  replacements?: Record<string, string>,
): string {
  let s = labels[key] ?? EN[key] ?? key;
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      s = s.replace(`{${k}}`, v);
    }
  }
  return s;
}
